"""Usando las propiedades de cadenas o strings"""

cadena = "Lenguaje de programación Python"

print("Tamaño del string: {}".format(len(cadena)))

print("La primera palabra de mi cadena es: {}".format(cadena[0:8]))

print("El últimmo valor de mi cadena es: {} y el penúltimo valor {}".format(cadena[-1], cadena[-2]))