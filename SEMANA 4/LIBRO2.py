"""Listas en Python"""

"""
Listas,
pop(i): Quita un elemento en una posición dada
"""

paises = ["Perú", "Canadá", "Argentina", "México"]

print("Mi lista inicial: {}".format(paises))

paises.pop()

print("Mi lista fiinal: {}".format(paises))

paises.pop(1)
print("Mi lista actualizada: {}".format(paises))