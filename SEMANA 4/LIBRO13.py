"""Diccionario en Python"""

"""
Los nombres de los key(llaves) van a ir escritos siempre en minúsculas (por converción)
"""

estudiante = {"nombre": "Matías", "edad": 26, "promedio":17}

"""Agregamos elementos en nuevo key a mi diccionario"""

estudiante['distrito'] = "San Isidro"
estudiante['habilitado'] = True
estudiante['nombre'] = "Fiorella"

print("Diccionario actualizado: {}".format(estudiante))